#include "libft.h"

int		ft_compt(int n)
{
	int	compt;

	compt = 0;
	if (n < 0)
		n = -n;
	else if (n == 0)
		return (1);
	while (n > 10)
	{
		n = n / 10;
		compt++;
	}
	return (compt);
}
