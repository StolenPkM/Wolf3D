#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int	i;
	int	j;

	j = 0;
	while (s1[j] != '\0')
		j++;
	i = 0;
	while (i < (int)n && s2[i])
		s1[j++] = s2[i++];
	s1[j] = '\0';
	return (s1);
}
